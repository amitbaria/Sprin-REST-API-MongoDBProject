package com.example.SpringBoot_mongoDB.model;

public class StudentDOB {
	
	private String dd,mm,yyyy;

	public String getDd() {
		return dd;
	}

	public void setDd(String dd) {
		this.dd = dd;
	}

	public String getMm() {
		return mm;
	}

	public void setMm(String mm) {
		this.mm = mm;
	}

	public String getYyyy() {
		return yyyy;
	}

	public void setYyy(String yyyy) {
		this.yyyy = yyyy;
	}
	
	

}
